                                                        .nomeCadastroFieldController
                                                        ?.clear();
                                                    _model
                                                        .emailCadastroFieldController
                                                        ?.clear();
                                                    _model
                                                        .passwordCadastroFieldController
                                                        ?.clear();
                                                    _model
                                                        .passwordConfirmarCadastroFieldController
                                                        ?.clear();
                                                  });

                                                  context.pushNamedAuth(
                                                      'LoginPage',
                                                      context.mounted);
                                                },
                                                text: 'Registrar-se',
